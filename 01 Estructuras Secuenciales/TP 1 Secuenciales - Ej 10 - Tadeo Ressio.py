# 10) Crear un programa que pida al usuario 3 números e imprima por pantalla el promedio de dichos números.
numeroA = float(input("Ingrese el primer número: "))
numeroB = float(input("Ingrese el segundo número: "))
numeroC = float(input("Ingrese el tercer número: "))
print(f"El promedio de los tres números es: {(numeroA + numeroB + numeroC) / 3}")