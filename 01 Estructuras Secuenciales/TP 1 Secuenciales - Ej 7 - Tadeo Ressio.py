# 7) Crear un programa que pida al usuario dos números enteros distintos del 0 y muestre por pantalla el resultado de sumarlos, dividirlos, multiplicarlos y restarlos.
numeroA = int(input("Ingrese el primer número entero distinto que 0: "))
numeroB = int(input("Ingrese el segundo número entero distinto que 0: "))
print(f"La suma de {numeroA} y {numeroB} es: {numeroA + numeroB}")
print(f"La resta de {numeroA} y {numeroB} es: {numeroA - numeroB}")
print(f"La multiplicación de {numeroA} y {numeroB} es: {numeroA * numeroB}")
print(f"La división de {numeroA} y {numeroB} es: {numeroA / numeroB}")